package day24_custom_methods;

public class Car {

    public static void unlockCar () {
        System.out.println("Unlocking car");
    }

    public static void OpenDorCar () {
        System.out.println("Open Door Car");
    }

    public static void sitInCar () {
        System.out.println("Sit in a car");
    }

    public static void closeDoor () {
        System.out.println("Close Door");
    }

    public static void puttheseatbelt () {
        System.out.println("put the seatbelt");
    }

    public static void startTheEngineRun () {
        System.out.println("Start The Engine and Run");
    }

    public static void main(String[] args) {
        unlockCar();
        OpenDorCar();
        sitInCar();
        closeDoor();
        puttheseatbelt();
        startTheEngineRun();
    }
}
