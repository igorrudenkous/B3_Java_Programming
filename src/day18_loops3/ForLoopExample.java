package day18_loops3;

public class ForLoopExample {
    public static void main(String[] args) {
        for (int i = 0; i <= 10; i ++) {


            System.out.println("This is \"do-while\" loop");
            int z = 0;
            do {
                System.out.println(z);
                z ++;
            } while (z <= 10);

        }
    }
}
