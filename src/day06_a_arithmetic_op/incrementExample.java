package day06_a_arithmetic_op;

public class incrementExample {
    public static void main(String[] args) {
        int x = 0;
        System.out.println(x);
        x =+ 1;
        System.out.println(x);
        x++;
        System.out.println(x);
        ++x;
        System.out.println(x);
    }
}
