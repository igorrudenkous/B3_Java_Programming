package day16_loops;

public class helloWorld {
    public static void main(String[] args) {


        int num = 1;
        while (num <= 10) {
            System.out.println("Hello World");
            num ++;
        }


        int num2 = 100;
        while (num2 <= 100) {
            System.out.println("Hello World2");
            num2++;
        }
    }
}
