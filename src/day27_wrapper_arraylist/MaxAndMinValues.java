package day27_wrapper_arraylist;

public class MaxAndMinValues {
    public static void main(String[] args) {
        // In each Any classes we can have methods and some other information as well
        // We can talk about something called CONSTANTS

        System.out.println(Byte.MIN_VALUE); // is CONSTANCE
        System.out.println(Byte.MAX_VALUE); // is CONSTANCE

        System.out.println(Integer.MIN_VALUE);
        System.out.println(Integer.MAX_VALUE);

        System.out.println(Double.MIN_VALUE);
        System.out.println(Double.MAX_VALUE);

        System.out.println(Long.MIN_VALUE);
        System.out.println(Long.MAX_VALUE);

    }
}
