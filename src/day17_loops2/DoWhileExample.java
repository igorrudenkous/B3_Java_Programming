package day17_loops2;

public class DoWhileExample {
    public static void main(String[] args) {
        int a2 = 0;

        do {
            System.out.println(a2);
            a2 += 5;
        } while (a2 <= 15);

        System.out.println("-----------------");
        int a3 = 0;
        do {
            System.out.println(a3);

        } while (a3 == 10);
    }
}
